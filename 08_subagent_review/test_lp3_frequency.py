"""
Tests for the LP3 function generated by orchestrate.py.
Run after orchestrate.py has written lp3_frequency.py.
"""
import math
import pytest
from lp3_frequency import fit_lp3

# 20-year record used across multiple tests (positive skew, realistic CFS)
FLOWS_20 = [
    800.0, 1100.0, 950.0, 1300.0, 870.0, 1450.0, 1020.0, 1200.0, 760.0, 1600.0,
    900.0, 1050.0, 1380.0, 980.0, 1250.0, 830.0, 1500.0, 1100.0, 940.0, 1700.0,
]


# ---------------------------------------------------------------------------
# Output structure
# ---------------------------------------------------------------------------

def test_returns_all_four_keys():
    result = fit_lp3(FLOWS_20)
    assert set(result.keys()) == {"return_2", "return_10", "return_50", "return_100"}


def test_return_periods_strictly_increasing():
    r = fit_lp3(FLOWS_20)
    assert r["return_2"] < r["return_10"] < r["return_50"] < r["return_100"], (
        "Longer return periods must produce higher discharge estimates"
    )


def test_all_estimates_positive():
    r = fit_lp3(FLOWS_20)
    for key, val in r.items():
        assert val > 0, f"{key} must be positive, got {val}"


# ---------------------------------------------------------------------------
# LP3 methodology
# ---------------------------------------------------------------------------

def test_100year_exceeds_observed_maximum():
    """100-year estimate must extrapolate above the observed record."""
    r = fit_lp3(FLOWS_20)
    assert r["return_100"] > max(FLOWS_20), (
        f"100-year ({r['return_100']:.1f}) must exceed max observed ({max(FLOWS_20):.1f})"
    )


def test_2year_estimate_near_geometric_mean():
    """For a log-symmetric record, Q2 should be close to the geometric mean."""
    r = fit_lp3(FLOWS_20)
    geo_mean = math.exp(sum(math.log(f) for f in FLOWS_20) / len(FLOWS_20))
    assert abs(r["return_2"] - geo_mean) / geo_mean < 0.30, (
        f"Q2 ({r['return_2']:.1f}) deviates >30% from geometric mean ({geo_mean:.1f})"
    )


# ---------------------------------------------------------------------------
# Unit passthrough
# ---------------------------------------------------------------------------

def test_cfs_scale_preserved():
    r = fit_lp3(FLOWS_20)
    assert 500 < r["return_2"] < 5000, "CFS-scale input should yield CFS-scale estimates"


def test_cms_scale_preserved():
    """Same relative record scaled to CMS should yield CMS-scale output."""
    flows_cms = [f / 35.315 for f in FLOWS_20]
    r = fit_lp3(flows_cms)
    assert r["return_2"] < 200, (
        f"CMS-scale input should yield CMS-scale estimates, got {r['return_2']:.2f}"
    )


def test_scaling_ratio_consistent():
    """Scaling all inputs by a constant should scale all outputs by the same constant."""
    scale = 10.0
    r1 = fit_lp3(FLOWS_20)
    r2 = fit_lp3([f * scale for f in FLOWS_20])
    for key in r1:
        ratio = r2[key] / r1[key]
        assert abs(ratio - scale) / scale < 0.05, (
            f"{key}: scaling ratio {ratio:.3f} != {scale} (>5% error)"
        )


# ---------------------------------------------------------------------------
# Edge cases
# ---------------------------------------------------------------------------

def test_short_record_raises():
    """Fewer than 10 observations must raise an exception."""
    with pytest.raises((ValueError, RuntimeError)):
        fit_lp3([100.0, 200.0, 300.0, 400.0, 500.0])


def test_zero_flows_handled_without_crash():
    """A single zero in the record should not cause a log-domain crash."""
    flows_with_zero = [0.0] + FLOWS_20
    try:
        result = fit_lp3(flows_with_zero)
        assert isinstance(result, dict)
    except (ValueError, RuntimeError):
        pass  # Raising a clear error is also acceptable


def test_negative_skew_100year_finite_and_positive():
    """With a negatively-skewed log record, Q100 must still be finite and positive."""
    # Negatively skewed: many high values with a few low outliers
    flows_neg_skew = [
        2000.0, 1900.0, 1800.0, 1850.0, 1750.0, 1920.0, 500.0, 1800.0,
        1780.0, 1830.0, 1700.0, 1950.0, 1810.0, 1760.0, 1840.0,
        1890.0, 1720.0, 1970.0, 1740.0, 400.0,
    ]
    r = fit_lp3(flows_neg_skew)
    assert r["return_100"] > 0 and math.isfinite(r["return_100"]), (
        f"Negative-skew Q100 must be finite positive, got {r['return_100']}"
    )


def test_large_record_runs_without_error():
    """50-year record should run cleanly."""
    import random
    random.seed(42)
    flows = [abs(random.gauss(1000, 300)) + 100 for _ in range(50)]
    r = fit_lp3(flows)
    assert len(r) == 4
